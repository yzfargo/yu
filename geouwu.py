import math

def calculate_hypotenuse(a, b):
    """Calculates the hypotenuse of a right-angled triangle."""
    return math.sqrt(a**2 + b**2)

def calculate_factorial(n):
    """Calculates the factorial of a number."""
    return math.factorial(n)

def calculate_pi_digits(digits):
    """Calculates the value of pi up to a specified number of digits."""
    return round(math.pi, digits)

# Example usage
a = 3
b = 4
hypotenuse = calculate_hypotenuse(a, b)
print(f"The hypotenuse of a triangle with sides {a} and {b} is: {hypotenuse}")

n = 5
factorial = calculate_factorial(n)
print(f"The factorial of {n} is: {factorial}")

digits = 10
pi_value = calculate_pi_digits(digits)
print(f"The value of pi to {digits} digits is: {pi_value}")
